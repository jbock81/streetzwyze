<script language="javascript">
    var str_assist = "Lorem ipsum dolor sit amet, consectetur adipisicing elit, sed do eiusmod tempor incididunt ut labore et dolore magna aliqua.";
    var reservation_id = "";
    $(document).ready(function(){

        $("#pda_assist").html("Provide mobile number to validate payment status");
        $("#btn_retrieve").click(function(e){
            var phone = $("#phone").val();
            var regex_phone = /^[0-9]+$/;
            if (!regex_phone.test(phone))
            {                
                $("#pda_assist").html("Validation error.");
                e.preventDefault();
                return;
            }
            else
            {

            }
            $.post(
                "admin-seller/ajax/getpaymentstatus.php",
                {
                    dispatch_mobile: phone
                },
                function(data){
                    var obj_data = JSON.parse(data);
                    $("#order_num").val(obj_data.orderNum);
                    $("#order_amount").val(obj_data.orderAmount);
                    $("#duration_remaining").val(obj_data.durationRemaining);
                    reservation_id = obj_data.reservationID;
                    $("#pda_assist").html("List of orders pending delivery confirmation");
                }
            );
        });

        $("#customer_code").hover(function(e){
            $("#pda_assist").html("Request for delivery code to disburse your payment");
        }, function(e){
            $("#pda_assist").html(str_assist);
        });
        $("#btn_delivery_code").click(function(e){
            var customer_code = $("#customer_code").val();

            $.post(
                "admin-seller/ajax/senddeliverycode.php",
                {
                    customerCode: customer_code,
                    reservationID: reservation_id
                },
                function(data){
                    $("#pda_assist").html(data);
                }
            );
        });
    });
</script>